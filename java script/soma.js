// Definindo os dois números
let numero1 = 5;
let numero2 = 3;

// Função para somar os dois números
function somar(a, b) {
    return a + b;
}

// Chamando a função e armazenando o resultado
let resultado = somar(numero1, numero2);

// Exibindo o resultado no console
console.log("A soma de " + numero1 + " e " + numero2 + " é: " + resultado);